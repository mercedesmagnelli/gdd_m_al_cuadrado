	JOIN M_AL_CUBO.PRODUCTO_X_VARIANTE_X_VENTA PVV ON V.VENTA_ID = PVV.VENTA_ID
	JOIN M_AL_CUBO.VARIANTE_PRODUCTO VP ON VP.VARIANTE_PRODUCTO_ID = PVV.VARIANTE_PRODUCTO_ID
